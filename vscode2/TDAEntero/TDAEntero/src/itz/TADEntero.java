package itz;

public class TADEntero {

//Estructura de datos
public final static int Cero=0;
//Metodos de la clase
public static int Sucesor(int valor) {
    return valor+1;
}
public static int Antecesor(int valor) {
    return valor-1;
}
public static int Suma(int valor1, int valor2) {
    return valor1+valor2;
}
public static int Diferencia(int valor1, int valor2) {
    return valor1-valor2;
}
public static int Producto(int valor1,int valor2) {
    return valor1*valor2;
}


}
